import Foundation

/// All gem types currently used in LepreCON.
///
/// This includes:
/// - six rainbow colors
/// - special gems like white, black, gold, clear, and pink
enum GemType: String, CaseIterable {
    case red
    case orange
    case yellow
    case green
    case blue
    case purple
    case white
    case black
    case gold
    case clear
    case pink
}

